                            <p>Exposure to the sun’s harmful UV rays causes wrinkles, age spots, and even skin cancer. Thus mid-day sun, between 10 am and 3 pm, should be avoided. However, if you still have to venture out under sunlight, a generous amount of sunscreen with a sun protection factor (SPF) greater than 15 with UVA and UVB coverage should be applied at least 20 minutes before going out. Moreover, while in sunlight, the sunscreen should be reapplied every 3-4 hours and after swimming. The use of wide-brimmed hats, long-sleeved shirts, and full pants while going in the sun can also reduce exposure to the sun.</p>
                            <p>Sun protection decreases vitamin D levels, so if levels are deficient, supplementation with Vitamin D should be considered.</p>
                            

                            <p>Body lift is a cosmetic procedure to shape and tone the underlying tissue, supporting the excessive fat and skin in the region of the breast, abdomen, buttocks, and thighs. Body lift involves removal of the excessive fat and sagging skin that results in poor tissue elasticity and hampers the personal appeal of the individual. The various procedures employed for body lift can be broadly categorised as:</p>                            

<h2>What is a Wellness Exam?</h2>

<p>A wellness exam involves routine tests usually performed once a year to check your overall health status. The type of tests ordered depends on your age, sex, diet, exercise, smoking habit, consumption of alcohol and current health condition.</p>

<h2>Preparing for your Wellness Exam</h2>

<p>Your healthcare provider will assess your symptoms and take your medical or surgical history. He/she will discuss the medicines you are taking and those you should stop taking prior to the tests. Inform your healthcare provider if you are allergic to any medicines or anaesthesia or have an implanted device such as a pacemaker.</p>

<h2>Common Wellness Tests</h2>

<h3>Physical Tests</h3>

<p>Your healthcare provider makes a note of your physical appearances such as height, weight, and any deformity or unusual growth on your body. Vital signs such as temperature, blood pressure, heart rate, and respiration rate will be checked. Your eyes, nose, throat, teeth, and lymph nodes on the side of the neck may be examined. Your doctor will listen to your heart and lungs to detect any abnormalities and asses your skin and extremities for any problems. Other systems may be assessed depending on the patient&acirc;&euro;&trade;s condition and history. Talk to your doctor about any health concerns you are having so they can address them during your exam.</p>

<h3>Laboratory Tests</h3>

<ul>
	<li><strong>Complete Blood Count:</strong> It measures several components of blood to evaluate your overall health and can help to diagnose anaemia, infections or certain disorders.</li>
	<li><strong>Urine Test:</strong> These tests are performed to look for certain nutrients, hormones, blood cells or microorganisms in the urine.</li>
	<li><strong>Cholesterol Screening:</strong> This test detects high levels of cholesterol in the blood that can lead to atherosclerosis, where cholesterol builds up in the artery walls causing heart disease.</li>
	<li><strong>Thyroid Function Test:</strong> It involves a series of blood tests to check how well your thyroid gland is working.</li>
	<li><strong>Electrocardiogram (ECG):</strong> An electrocardiogram is a diagnostic tool to measure and record the electrical activity of the heart to assess its functionality.</li>
	<li><strong>Metabolic Stress Test:</strong> Metabolic stress testing is a screening tool used to evaluate cardiopulmonary function.</li>
	<li><strong>Pulmonary Function Test:</strong> Also called spirometry or lung function test, it measures your ability to breathe and how efficiently gas is exchanged between your lungs and the rest of your body. Various lung conditions such as asthma, bronchitis, lung infections, emphysema, allergies, and lung cancer can be diagnosed by pulmonary function testing.</li>
	<li><strong>Liver Function Test:</strong> This test determines the health of your liver, gallbladder and bile ducts.</li>
	<li><strong>Imaging Tests:</strong> Imaging techniques such as X-ray, CT-scan or MRI help your doctor to view images of the internal structures of the body.</li>
	<li><strong>Bone Density:</strong> A bone density test is performed in all postmenopausal women with fractures. Women under the age of 65, depending on their risk factors, should be screened for osteoporosis.</li>
</ul>

<p>Patients between the ages of 50 and 75 need to be screened for colon cancer. A stool test is recommended every year. Flexible sigmoidoscopy and barium enema are recommended every 5 years and a colonoscopy is recommended every 10 years. Additional studies may be scheduled by your doctor if you have a history of ulcerative colitis or any family history of colon cancer.</p>

<h2>Male Wellness Exam</h2>

<p>Wellness exams for men include an examination of:</p>

<ul>
	<li><strong>Groin Area:</strong> Your healthcare provider will examine the area around the groin and testicles to check for signs of a hernia. Your doctor will instruct you to cough while checking for a bulge.</li>
	<li><strong>Testis:</strong> Your healthcare provider will check for lumps, tenderness or a change in the shape of the testicles.</li>
	<li><strong>Penis:</strong> Your doctor will check for the presence of ulcers or warts to assess for signs of any sexually transmitted diseases (STDs).</li>
	<li><strong>Prostate:</strong> A gloved, lubricated finger is inserted into the rectum to feel for any lumps or enlargement of the prostate.</li>
</ul>

<h2>Female Wellness Exam</h2>

<p>Wellness exams for women include an examination of:</p>

<ul>
	<li><strong>Breasts:</strong> Women should perform a monthly self-exam of their breasts. Any lumps or other abnormalities noted in the breasts should immediately be reported to the doctor. Women between the ages of 20 to 40 need to have their breasts examined by a doctor once every 3 years, and a complete breast exam is recommended every year in women aged 40 and above. Depending on their risk factors for breast cancer, women over the age of 40 need to have a mammogram performed every 1 to 2 years.</li>
	<li><strong>Pelvis: </strong>Screening will begin within 3 years after the first vaginal intercourse or by the age of 21. After the age of 21, women should have both a Pap smear and a pelvic exam performed every 2 years to check for cervical cancer. Women over age 30 or whose Pap smears have been negative three times in a row may only need a Pap smear once every 3 years. Sexually active women should be screened for sexually transmitted diseases.</li>
</ul>


                            <h2>What is Gynecomastia?</h2>
                            <p>Gynecomastia is an abnormal condition characterised by breast enlargement in males. Occasionally they may also secrete milk. This condition may also be seen in neonates, adolescents, and elderly individuals, as a part of physiological changes.</p>
                            <p>Gynecomastia can affect one or both breasts. Generally, gynecomastia does not produce any harmful effect on the body but it may induce undue stress and embarrassment that can impact the active sex life of the affected individual. Gynecomastia can also cause psychological damage or loss of self- esteem in adolescents.</p>

                            <h2>Causes of Gynecomastia</h2>
                            <p>Based on the physiological and pathological factors responsible for gynecomastia, the common causes of gynecomastia have been listed below:</p>
                            <h3>Physiological</h3>
                            <p>The physiological changes responsible for gynecomastia are commonly seen in the following age groups:</p>
                            <ul>
                                <li><strong>Newborn:</strong> Due to the presence of maternal hormones circulating in the blood</li>
                                <li><strong>Adolescence:</strong> Due to hormonal changes which are temporary and may disappear as the hormonal levels stabilise</li>
                                <li><strong>Increased age and weight:</strong> Production of testosterone declines with age while fat cells stimulate the secretion of the female hormone oestrogen.</li>
                            </ul>
                            <h3>Pathological</h3>
                            <p>The various pathological causes of gynecomastia are as follows:</p>
                            <ul>
                                <li>Certain medication such as oral contraceptive pills, antipsychotics, and anabolic steroids cause gynecomastia as a side effect </li>
                                <li>Increased levels of oestrogen due to various conditions such as obesity, hyperthyroidism, malnourishment, adrenal tumours, and testicular tumours </li>
                                <li>Decreased or lack of testosterone production due to conditions such as Klinefelter's syndrome, viral orchitis, and renal failure</li>
                                <li>Chronic kidney or liver disease</li>
                                <li>HIV infection</li>
                                <li>Chronic illness</li>
                            </ul>
                            <p>In some cases gynecomastia may be induced secondary to spinal cord injury or refeeding after starvation; however, the underlying cause for this form of gynecomastia remains unknown.</p>

                            <h2>Diagnosis of Gynecomastia</h2>
                            <p>The diagnosis of gynecomastia includes a physical examination of the patient, by the physician. Occasionally a mammogram may be ordered to confirm the diagnosis of gynecomastia. Certain blood tests may also be conducted to evaluate the underlying cause of gynecomastia. </p>
                            
                            <h2>Treatment for Gynecomastia</h2>
                            <p>The non-surgical methods employed for the management of gynecomastia include:</p>
                            <ul>
                                <li>In case of drug induced gynecomastia, replacement of the offending drug with an alternative and safer medication</li>
                                <li>Medical treatment of the underlying disease conditions causing gynecomastia </li>
                            </ul>
                            <p>In patients not responding to non-surgical options, surgery is considered as the last option. The surgical methods for management of gynecomastia include:</p>
                            <ul>
                                <li>Liposuction</li>
                                <li>Gland excision</li>
                                <li>Skin sculpture</li>
                                <li>Reduction mammoplasty</li>
                                <li>Combination of the surgical techniques mentioned above</li>
                            </ul>
                            <p>The basic steps involved in the surgery are as follows:</p>
                            <ul>
                                <li>The surgery can be performed under general or local anaesthesia.</li>
                                <li>The patient is anaesthetised. </li>
                                <li>A small incision is made below the areola region of the breast.</li>
                                <li>Excessive breast tissue is surgically removed. </li>
                                <li>Excessive fat is removed by liposuction.</li>
                                <li>The incision is sutured closed.</li>
                            </ul>
                            
                            <p>Normally the surgery takes about 90 minutes. After surgery patients are often advised to wear an elastic pressure garment or ace bandage that promotes healing. Patients are also advised on rehabilitation exercises for at least 3 weeks and can return to normal activities in 6 weeks.</p>
                            

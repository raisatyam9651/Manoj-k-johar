                            <p>Non-surgical fat reduction, also known as non-invasive body contouring, is a fat reduction procedure that uses non-invasive technology such as cold energy, heat energy, sound waves, or an injected medication to reduce or shrink stubborn fat pockets (fat deposits underneath the skin, but above the muscle) from specific areas of the body to achieve contour and shape.</p>

					<h3>When can I exercise after plastic surgery?</h3>
					<p>Light walking is usually encouraged within the first few days after surgery to promote circulation. Strenuous exercise, including weightlifting and high-impact activities, should be avoided for four to six weeks or until Dr. Johar gives you clearance. Always listen to your body and start gradually.</p>

					<h3>How long do I need to take off work after plastic surgery?</h3>
					<p>Most patients need one to four weeks off work, depending on the procedure and the physical demands of their job. Procedures like rhinoplasty or eyelid surgery may require less downtime, while a tummy tuck or facelift may require two to four weeks. Dr. Johar will provide a personalized timeline during your consultation.</p>

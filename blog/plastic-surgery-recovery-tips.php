					<h3>Is it normal to feel emotional after surgery?</h3>
					<p>Yes, it is completely normal. The "post-surgery blues" are common and usually resolve within a few days to a week. Swelling, bruising, and the adjustment period can be emotionally challenging. Talking to a trusted person, staying connected with your surgical team, and practicing self-care can help.</p>

				<div class="faq-item">
					<h3>When will my scars fade?</h3>
					<p>Scars go through several stages of healing. They typically appear red or raised initially, then gradually flatten and fade over six to twelve months. Using sunscreen on scars, applying silicone scar sheets, and following Dr. Johar's scar care instructions can help minimize their appearance.</p>
				</div>


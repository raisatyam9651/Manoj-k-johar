							<div class="faq-item">
								<h3>Q: When can I exercise after nose surgery?</h3>
								<p>A: Light walking can be resumed after about two weeks. Moderate exercise, such as jogging or cycling, can typically begin around 4-6 weeks. Strenuous exercise and contact sports should be avoided for at least 3-4 months. Always follow Dr. Johar's specific recommendations.</p>
							</div>

							<div class="faq-item">
								<h3>Q: When will the swelling completely go away?</h3>
								<p>A: Most swelling resolves within the first 2-4 weeks. However, subtle swelling, especially at the nasal tip, can persist for 6-12 months. The final results of rhinoplasty are typically visible at the 12-month mark.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Can I wear glasses after rhinoplasty?</h3>
								<p>A: You should avoid resting glasses on the bridge of your nose for at least 4-6 weeks after surgery to prevent pressure on the healing bones. You can use medical tape on the bridge of the nose to support glasses, or switch to contact lenses during the recovery period.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Is it normal to feel congested after nose surgery?</h3>
								<p>A: Yes. Nasal congestion is very common after rhinoplasty and is caused by internal swelling. It typically improves significantly within the first two weeks and continues to resolve over the following months. Do not attempt to force your nasal passages open.</p>
							</div>

							<div class="faq-item">
								<h3>Q: When can I fly after rhinoplasty?</h3>
								<p>A: It is generally recommended to wait at least 1-2 weeks before flying after rhinoplasty. Changes in cabin pressure can increase nasal swelling and discomfort. Consult Dr. Johar for personalized advice based on your recovery timeline.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Will I have visible scars after nose surgery?</h3>
								<p>A: In closed rhinoplasty, there are no external scars. In open rhinoplasty, a small scar on the columella is usually very faint and becomes nearly invisible over time. Proper wound care and sun protection help minimize scarring.</p>

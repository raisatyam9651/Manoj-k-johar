							<div class="faq-item">
								<h3>Q: What is the minimum age for plastic surgery?</h3>
								<p>A: There is no universal minimum age, but most cosmetic procedures are recommended for patients who are at least 18 years old and have completed their physical development. Reconstructive procedures may be performed at any age depending on medical need. Dr. Johar evaluates each patient individually to determine the appropriate timing.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Can I have plastic surgery if I smoke?</h3>
								<p>A: Smoking significantly increases the risk of complications during and after surgery, including poor wound healing, infection, and tissue death. Most surgeons, including Dr. Johar, require patients to quit smoking at least four to six weeks before and after surgery. If you are a smoker, quitting temporarily can greatly improve your safety and results.</p>
							</div>

							<div class="faq-item">
								<h3>Q: How do I know if I have realistic expectations?</h3>
								<p>A: A good indicator is whether you can describe what you want to improve in specific, measurable terms and whether you understand that results will be an improvement, not perfection. During your consultation, Dr. Johar will help you understand what is achievable and ensure your expectations align with realistic outcomes.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Should I lose weight before having plastic surgery?</h3>
								<p>A: For body contouring procedures like liposuction and tummy tucks, it is best to be at or near your ideal weight before surgery. These procedures remove excess fat and skin but are not weight loss solutions. Stable weight ensures the best and most lasting results.</p>
							</div>

							<div class="faq-item">
								<h3>Q: What if I'm not sure which procedure I need?</h3>
								<p>A: That is perfectly fine. During your consultation, Dr. Johar will evaluate your concerns, discuss your goals, and recommend the most appropriate procedure or combination of procedures. You do not need to have all the answers before your first visit.</p>
							</div>

							<div class="faq-item">
								<h3>Q: How long should I wait after a major life change before having surgery?</h3>
								<p>A: It is generally recommended to wait until you have fully recovered from any major life event — such as a divorce, loss of a loved one, or significant career change — before undergoing elective surgery. Emotional stability is important for a positive surgical experience and recovery.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Can plastic surgery help with mental health concerns like low self-esteem?</h3>
								<p>A: While many patients report improved self-confidence after surgery, plastic surgery is not a treatment for mental health conditions. If you are struggling with body dysmorphic disorder or severe self-esteem issues, it is important to seek support from a mental health professional in addition to consulting a plastic surgeon.</p>

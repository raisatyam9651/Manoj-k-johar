<h2>Frequently Asked Questions</h2>

<div class="faq-item">
<h3>How far in advance should I stop taking blood thinners before surgery?</h3>
<p>Blood thinners such as aspirin, warfarin, and clopidogrel typically need to be stopped seven to ten days before surgery. However, the exact timeline depends on the specific medication and your medical condition. Always consult with Dr. Johar and your prescribing physician before making any changes to your medication regimen.</p>
</div>

<div class="faq-item">
<h3>Can I eat before plastic surgery?</h3>
<p>You should follow the specific fasting instructions provided by your surgical team. Generally, you should stop eating solid foods at least eight hours before surgery. Clear liquids may be permitted up to two to four hours before the procedure. Following these guidelines exactly is essential for your safety during anesthesia.</p>
</div>

<div class="faq-item">
<h3>What should I wear to the surgical facility on the day of surgery?</h3>
<p>Wear loose, comfortable clothing with buttons, zippers, or snaps in the front. This makes it easier to change into and out of surgical gowns and post-operative clothing. Avoid wearing jewelry, makeup, nail polish, or contact lenses. Remove all piercings before arriving at the facility.</p>
</div>

<div class="faq-item">
<h3>How long do I need someone to help me after surgery?</h3>
<p>The amount of help needed depends on the type of procedure. For minor procedures, you may only need assistance for the first 24 to 48 hours. For more extensive surgeries like body contouring or breast augmentation, you may need help for one to two weeks. Dr. Johar will provide a personalized estimate based on your specific procedure.</p>
</div>

<div class="faq-item">
<h3>When can I return to work after plastic surgery?</h3>
<p>Return to work depends on the procedure and your type of work. Patients with desk jobs may return within one to two weeks for minor procedures. More extensive surgeries may require two to six weeks of recovery. Dr. Johar will provide a specific timeline during your pre-operative consultation.</p>
</div>

<div class="faq-item">
<h3>Should I stop exercising before surgery?</h3>
<p>Regular exercise is encouraged up until a few days before surgery, as it promotes overall health and can aid in recovery. However, you should stop strenuous exercise at least three to five days before surgery to allow your body to rest and reduce inflammation. Follow Dr. Johar's specific recommendations regarding exercise before your procedure.</p>
</div>

<div class="faq-item">
<h3>Do I need to prepare anything special at home before surgery?</h3>
<p>Yes, preparing a comfortable recovery area at home is important. Set up a bed or recliner with pillows, fill prescriptions in advance, stock up on healthy foods and water, and have entertainment items within reach. Arrange for household help and childcare if needed, and ensure that someone can drive you home after surgery.</p>
</div>

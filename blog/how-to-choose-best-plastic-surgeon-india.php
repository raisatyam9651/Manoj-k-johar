							<h2>Frequently Asked Questions</h2>

							<div class="faq-item">
								<h3>Q: How do I verify a plastic surgeon's credentials in India?</h3>
								<p>A: You can verify a surgeon's credentials by checking their MBBS, MS, and M.Ch/DNB qualifications, and by looking up their registration number on the National Medical Commission (NMC) or State Medical Council website. Professional memberships in APSI or IAAPS are additional indicators of credibility.</p>
							</div>

							<div class="faq-item">
								<h3>Q: What is the difference between a plastic surgeon and a cosmetic surgeon?</h3>
								<p>A: A plastic surgeon is trained in both reconstructive and cosmetic procedures and has completed a super-specialization in plastic surgery. A cosmetic surgeon may not have the same level of training and may only perform aesthetic procedures. Always choose a board-certified plastic surgeon for surgical procedures.</p>
							</div>

							<div class="faq-item">
								<h3>Q: How many consultations should I have before choosing a surgeon?</h3>
								<p>A: It is recommended to have at least two or three consultations with different surgeons before making a final decision. This allows you to compare approaches, communication styles, and pricing to find the surgeon who best meets your needs.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Should cost be the most important factor when choosing a surgeon?</h3>
								<p>A: No. While cost is a consideration, it should never be the primary factor. Patient safety, surgeon qualifications, experience, and facility quality are far more important. Choosing a less expensive option that compromises on these factors can lead to poor results and additional costs for revision surgery.</p>
							</div>

							<div class="faq-item">
								<h3>Q: Can I see before-and-after photos during the consultation?</h3>
								<p>A: Yes, most reputable surgeons maintain a portfolio of before-and-after photos. Ask to see results for the specific procedure you are considering. A good surgeon will be happy to share their work and help you set realistic expectations.</p>
							</div>

							<div class="faq-item">
								<h3>Q: How do I know if a clinic is safe for surgery?</h3>
								<p>A: Check for accreditation from recognized bodies such as NABH or JCI. Ensure the clinic has modern operating rooms, advanced equipment, and emergency protocols. Ask about the anesthesia team and their qualifications. A clean, well-organized facility is a strong indicator of safety.</p>
							</div>

							<div class="faq-item">
								<h3>Q: What questions should I ask during my first consultation?</h3>
								<p>A: Ask about the surgeon's qualifications, experience with the procedure you want, expected results, recovery time, potential risks, total cost, and follow-up care. Also ask to see before-and-after photos of similar cases. A good surgeon will answer all your questions openly and honestly.</p>

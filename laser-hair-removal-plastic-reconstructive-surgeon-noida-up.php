                            <h2>What is Laser Hair Removal?</h2>
                            <p>Unwanted hair is a common problem affecting both men and women. Conventional hair removal techniques such as waxing, threading, bleaches and depilatory creams offer only a temporary solution for unwanted hair. Laser hair removal treatment can be used to permanently treat and remove unwanted hair from the body.</p>
                            <p>Laser hair removal uses light energy from a laser to remove unwanted body hair by heating the hair follicle and damaging it to prevent hair regrowth. It is a very popular non-surgical cosmetic procedure. According to the FDA, permanent hair reduction is defined as long term, stable reduction in the amount of hair re-growing after a treatment regime.</p>

                            <h2> Stages of Hair Growth</h2>
                            <p>Hair growth occurs in 3 stages:</p>
                            <ul>
                                <li><strong>Anagen: </strong>hair is actively growing</li>
                                <li><strong>Catagen:</strong> hair growth stops and the follicle shrinks</li>
                                <li><strong>Telogen:</strong> hair growth resumes forcing 'old' hair out of the follicle to make room for the new hair.</li>
                            </ul>
                            <p>Hair will respond more effectively to laser treatment in the anagen stage. Therefore, it is necessary to treat an area at least 3 times with the treatments spaced approximately 6 weeks apart.</p>
                            
                            <h2>Procedure for Laser Hair Removal</h2>
                            <p>The procedure may take several minutes to several hours depending on the size of the area being treated. The area to be treated is cleaned, shaved, and anaesthetic cream is applied to lessen discomfort. This technique uses a low energy laser. The laser produces pulsed beams of highly concentrated light that pass through the skin and is absorbed by the pigment in the hair follicle. This disables the hair follicle in the deeper layer of the skin.</p>
                            
                            <h2> Indications of Laser Hair Removal</h2>
                            <p>Laser hair removal may be indicated in various conditions including hirsutism, hypertrichosis, acne keloidalis nuchae, pilonidal sinus or cyst, and pseudofolliculitis barbae.</p>
                            
                            <h2>Contraindications of Laser Hair Removal System</h2>
                            <p>It is contraindicated in conditions of pregnancy, epilepsy and herpes simplex viral infections. It is not recommended for use in patients with diabetes and on areas of sunburned skin.</p>
                            
                            <h2>Complications of Laser Hair Removal</h2>
                            <p>Laser hair removal is associated with certain complications such as hypo- or hyperpigmentation (skin colour changes), skin irritation, infection, and scarring at the treatment site. There is no guarantee that the hair follicle will respond to the laser treatment and multiple sessions may be required to achieve optimum results.</p>
                            
                            <h2> Side effects of Laser Hair Removal</h2>
                            <p>Following the treatment, some patients may experience side effects such as redness, itching, scarring, inflammation, and appearance of blisters, burning or prickling sensation at the site of treatment. If these effects are severe, consult your dermatologist immediately.</p>
                            
                            <h2>Preparation guidelines</h2>
                            <ul>
                                <li>Avoid sunbathing for at least 4 weeks before treatment.</li>
                                <li>Avoid waxing, plucking, electrolysis before treatment.</li>
                                <li>Stop using lotions or sprays 8 to 10 days before treatment.</li>
                                <li>Wash off all make-up before treatment.</li>
                            </ul>
                            <h2>Precautions after treatment</h2>
                            <ul>
                                <li>Avoid using hot tubs and saunas during the healing process.</li>
                                <li>Cleanse the treated area gently with mild soap and water and avoid picking or scrubbing at these areas.</li>
                                <li>Keep the area cool by applying cool gel packs on the treated areas over a towel.</li>
                                <li>Avoid the use of deodorant if the underarm area was treated.</li>
                                <li>Avoid direct sun exposure and tanning beds for at least a week.</li>

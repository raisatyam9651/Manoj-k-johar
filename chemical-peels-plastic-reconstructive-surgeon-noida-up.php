                            <h2>What are Chemical Peels?</h2>
                            <p>A chemical peel is a skin rejuvenation technique used to remove the top layers of the skin through the application of a chemical solution. The new skin that grows back in place of the old skin will be smoother and more youthful in appearance.</p>

                           <h2>Types of Chemical Peels</h2>
                            <ul>
                                <li><strong>Light chemical peel:</strong> This is used for the removal of the epidermis, the outermost layer of the skin, using a superficial chemical peel. </li>
                                <li><strong>Medium chemical peel:</strong> This is used to remove the epidermis as well as portions of the dermis, the middle layer of the skin. </li>
                                <li><strong>Deep chemical peel:</strong> This is a more aggressive removal of skin cells, intended to have a longer-lasting effect.</li>
                            </ul>
                            
                            <h2>Indications for Chemical Peels</h2>
                            <p>Chemical peels can be used to treat:</p>
                            <ul>
                                <li>Fine lines near the eyes and mouth</li>
                                <li>Wrinkles due to ageing or sun damage</li>
                                <li>Mild scarring or acne scars</li>
                                <li>Freckles, age spots, or dark patches </li>
                                <li>Hyperpigmentation</li>
                            </ul>
                            
                            <h2>Preparation for Chemical Peels</h2>
                            <ul>
                                <li>Your face will be cleaned and you will be provided with goggles and a headcover to protect your eyes and hair.</li>
                                <li>Your doctor may advise you to prepare your skin by using certain medications and drugs.</li>
                                <li>You may be administered a sedative or topical anaesthetic depending on the type of peel procedure.</li>
                            </ul>
                            
                            <h2>Chemical Peel Procedure</h2>
                            <p>During a light chemical peel:</p>
                            <ul>
                                <li>Your doctor will apply the chemical solution, usually containing glycolic or salicylic acid, over your skin using a brush, sponge, or gauze.</li>
                                <li>As the treated skin begins to whiten, you may feel a slight stinging sensation.</li>
                                <li>Your doctor will apply a neutralising solution or wash to remove the chemical solution from the treated skin.</li>
                            </ul>
                            
                            <p>During a medium chemical peel:</p>
                            <ul>
                                <li>Your doctor will use a cotton-tipped applicator or gauze to apply a chemical solution containing trichloroacetic acid, sometimes in combination with glycolic acid, after which the skin will begin to whiten.</li>
                                <li>Your doctor will apply cool compresses to soothe your skin afterwards. You may also be provided with a handheld fan to cool your skin.</li>
                                <li>You may feel a stinging sensation for about 20 minutes.</li>
                            </ul>
                            
                            <p>During a deep chemical peel:</p>
                            <ul>
                                <li>You will be given intravenous (IV) fluids, and your heart rate will be closely monitored.</li>
                                <li>Your doctor will apply carbolic acid and phenol on your skin using a cotton-tipped applicator, after which your skin will begin to turn white or grey.</li>
                                <li>To limit exposure to phenol, the procedure will be done in portions at about 15-minute intervals. A full-facial procedure can take about 90 minutes.</li>
                            </ul>
                            
                            <h2>Post-operative Care</h2>
                            <p>Your skin will generally be slightly red and swollen after the procedure and your doctor may recommend sun protection, cleansers, moisturisers, and protective ointments to apply to the skin. You should use ice packs to soothe your skin and avoid picking, rubbing, or scratching the treated area. Depending on the type of chemical peel, your skin may take a week to a few months to heal completely, and for the full results of the procedure to become visible.</p>
                            
                            <h2>Side-Effects of Chemical Peels</h2>
                            <p>Various side-effects that may be caused by a chemical peel include:</p>
                            <ul>
                                <li>Redness in the treated skin</li>
                                <li>Scarring, usually around the lower region of your face.</li>
                                <li>Skin becomes darker than normal (hyperpigmentation) or lighter than normal (hypopigmentation). </li>
                                <li>Bacterial, fungal, or viral infection</li>
                            </ul>

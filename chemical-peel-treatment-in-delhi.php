         <h3>Types of Chemical Peels</h3> 
         
         
          <center> <img src="/images/Types of Chemical Peels.png" alt="Types of Chemical Peels" style="width:90%; max-width:500px; margin-bottom:30px;"> </center>  
         
         
         <p>Chemical peels are categorised based on their intensity and target skin concerns. Superficial peels are designed to treat mild skin imperfections such as dullness and uneven tone, using mild acids like alpha-hydroxy acids (AHAs). They have minimal downtime and are ideal for subtle rejuvenation. Medium peels address fine lines, wrinkles, and moderate pigmentation issues, utilising trichloroacetic acid (TCA) and requiring about 5-7 days for recovery. Deep peels target severe wrinkles, scars, and sun damage, employing strong acids like phenol, offering dramatic results but requiring a longer recovery period. Dr. Manoj K Johar assesses your skin type and concerns to recommend the most suitable peel for you.</p>
                        
                        <h3>The Chemical Peel Procedure</h3>  
                        
                        <p>The chemical peel procedure begins with an initial consultation, where Dr. Johar evaluates your skin and discusses your goals. A personalised treatment plan is crafted based on your skin’s needs. During the preparation phase, your skin is cleansed thoroughly to remove impurities, and a protective barrier may be applied to sensitive areas. The chemical solution is then applied to your skin, with the duration depending on the type and depth of the peel. Once the application is complete, the solution is neutralised, and soothing agents are applied to minimise irritation. Post-treatment instructions are provided to ensure optimal results.</p>
                        
                        <h3>Pre-Treatment Care</h3>
                        
                        <p>Proper pre-treatment care is essential for the success of a chemical peel. Avoid direct sun exposure for at least two weeks before the procedure to protect your skin. Discontinue the use of retinoids or exfoliants a week prior to the treatment, and keep your skin hydrated and moisturised. It is also crucial to share your medical history and skincare routine with Dr. Johar to ensure the treatment is safe and effective.</p>
                        
                        <h3>Post-Treatment Care</h3>
                        
                        <p>Post-treatment care plays a vital role in achieving the best results. Use a gentle moisturiser to keep your skin hydrated and apply broad-spectrum sunscreen to prevent sun damage. Refrain from using scrubs or strong skincare products for at least a week after the treatment. Allow your skin to peel naturally without interference, and attend follow-up appointments with Dr. Johar to monitor your progress and address any concerns.</p>
                        
                        <h3>Target Skin Conditions</h3>
                        

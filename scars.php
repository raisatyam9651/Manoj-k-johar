<p>Scars form as part of the healing process after your skin has been cut or damaged. The skin repairs itself by growing new tissue to pull together the wound and fill in any gaps caused by the injury. Scar tissue is made primarily of a protein called collagen.</p>

<p>Scars develop in all shapes and sizes. Some scars are large and painful, while some are barely visible.</p>

<p>Not all scars require treatment, and many fade away over time. If a scar is bothering you or causing pain, treatments can help.</p>

<h2>What are the types of scars?</h2>

<p>Scars can develop anywhere on the skin. There are several types of scars, including:</p>

<ul>
	<li><strong>Contracture:&nbsp;</strong>Often developing after a burn, a contracture scar causes the skin to tighten (contract). These scars can make it difficult to move, especially when the scarring gets into the muscles and nerves or occurs over a joint.</li>
	<li><strong>Depressed (atrophic):&nbsp;</strong>These sunken scars often result from chickenpox or acne. They look like rounded pits or small indentations in the skin. Also called ice pick scars, they develop most often on the face.&nbsp;Acne scars&nbsp;may become more noticeable as you age because the skin loses collagen and elasticity over time.</li>
	<li><strong>Flat:&nbsp;</strong>Although it may be slightly raised at first, this type of scar flattens out as it heals. Flat scars are often pink or red. Over time, they may become slightly lighter or darker than the surrounding skin.</li>
	<li><strong>Keloids:</strong>&nbsp;These scars are raised above the skin&rsquo;s surface and spread beyond the wounded area. The overgrown scar tissue can get large and may affect movement.</li>
	<li><strong>Raised (</strong><strong>hypertrophic</strong><strong>):</strong>&nbsp;You can feel a hypertrophic scar when you run your finger over it. These raised scars may get smaller over time, but they never completely flatten out. Unlike keloids, they don&rsquo;t grow or spread beyond the wounded area.</li>
	<li><strong>Stretch marks:</strong>&nbsp;When skin expands or shrinks quickly, the connective tissues under the skin can be damaged.&nbsp;Stretch marks&nbsp;often develop during pregnancy, puberty or after gaining or losing a lot of weight. They usually appear on the breasts, stomach, thighs and upper arms.</li>
</ul>


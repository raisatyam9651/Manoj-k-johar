                            <p><b>Lymphedema </b>is a condition in which there is an increase in the size of the extremity (upper limbs, lower limbs) that is caused by partial or total obstruction of the lymphatic channels. In the beginning, this increase in size is due to lymph accumulation. With time, if left untreated, this accumulated lymph triggers an increase in the supporting tissues of the affected limbs. It not only causes limb size to increase on a more permanent basis but also reduces oxygen availability to the tissues. It interferes with wound healing and provides a good environment for an infection that happens frequently. Every such episode further increases the irreversible swelling.</p>
                            <h2>Causes of Lymphedema </h2>
                            <ul>
                                <li>Primary Lymphedema is when lymphatic vessels are missing or impaired. It can happen at birth, develop at the onset of puberty, or in adulthood. These people are born with genes that risk developing Lymphedema because lymph nodes or vessels are missing or not working correctly.</li>
                            </ul>
                            <ul>
                                <li>Secondary Lymphedema is when lymph vessels are damaged or lymph nodes are removed. Infection (Filariasis), surgeries, injuries, and obesity are some of the causes of lymph vessel and lymph node damage. Some <b>Cancer treatments</b> that are necessary to save lives can cause damage to the lymphatics. <b> Lymphedema </b>can occur even many years after the surgery or damage.</li>
                            </ul>
                            <h2>Symptoms of Lymphedema </h2>
                            <ul>
                                <li>A feeling of heaviness in a body part, usually a limb</li>
                                <li>A tight feeling in the skin of the affected limb; watches or rings may feel tight</li>
                                <li>A heavy swollen limb</li>
                                <li>Chronic swelling</li>
                                <li>Discolouration of skin overlying the Lymphedema </li>
                                <li>Deformity (elephantiasis)</li>

                                <li> Less movement or exibility in hand, wrist, or ankle</li>
                                <li>Severe /Easy / Early fatigue</li>
                            </ul>

                            <h2>Structure of skin</h2>
                            <p>The skin is made up of three layers— the epidermis, dermis, and subcutis. The epidermis is the outermost layer and acts as a physical barrier between the external environment and the inner structures of the body. The epidermis is constantly replaced, every two weeks, as the top layer of dead cells is shed and new cells from the lower layer move up to replace the dead cells. Dermis lies below the epidermis and provides strength, elasticity, and toughness to the skin. It is primarily composed of collagen fibres, interspersed with elastin fibres, embedded in a gelatinous matrix made of proteoglycans that bind water molecules and keep the skin hydrated. All the three structural components of the dermis are formed by the fibroblast cells present in the dermis. The dermis also contains blood vessels, nerve cells and specialised cells such as hair follicles, sebaceous glands, sweat glands and apocrine glands (scent glands).  Subcutis is a layer of fat and connective tissue present below the dermis. It contains blood vessels and nerves, larger than those present in the dermis, and serves as a storage of fat.</p>
                            
                            <h2>Changes with ageing</h2>                            
                            <p>With ageing, major changes occur in the dermis. The ageing fibroblasts lose their normal structural and functional ability, producing less collagen. This slowly decreases the proportion of collagen fibres present in the dermis. Moreover, the production of collagenase, an enzyme that breaks down the collagen, is also increased, resulting in the fragmentation of the existing fibres.  This collapse of the collagen structure, with age, slowly leads to the appearance of wrinkles.</p>
                            

                            <h2>What is Skin Cancer/ Melanoma?</h2>
                            <p>Skin cancer is the most common type of cancer. It can be classified as melanoma and non-melanoma skin cancers. The melanomic form is the more serious and potentially fatal skin cancer that begins in melanocytes (cells that give the skin its colour). The non-melanomic form of skin cancer occurs in cells that are located just below the outer layer of the skin. Unlike the melanomic form, this cancer does not spread to other parts of the body.</p>
                            <p>Self-examination of the body at regular intervals and examination by the doctor during routine check-ups are important for the early detection and successful treatment of skin cancers. Moles, although mostly harmless, are important indicators. A normal mole can be round, oval, flat or raised, usually evenly coloured tan, black or brown, and generally less than a quarter of an inch in size.</p>
                            
                            <p>Changes in the size, shape or colour of a mole, or the development of a new mole can indicate skin cancer. All parts of the body from head to toe, including hard-to-see areas such as the lower back and back of the thighs, should be examined for any abnormalities or changes in how the moles look over a course of time. There is a simple rule called the ABCDE rule, which helps you to realise whether the changes observed in a particular mole are indicative of skin cancer.</p>
                            
                            <ul>
                                <li><strong>A</strong> stands for Asymmetry: Abnormal moles, which can turn out to be cancerous, are asymmetrical, i.e. one half is not identical to the other.</li>
                                <li><strong>B</strong> stands for Border irregularity: The border of the abnormal mole is uneven and rough.</li>
                                <li><strong>C</strong> stands for Colour: The colour across the abnormal mole can spread beyond the border/edge of the mole or may vary in shades of tan, brown, black or red, giving a mottled appearance.</li>
                                <li><strong>D</strong> stands for Diameter: The diameter of the abnormal mole exceeds 6 mm.</li>
                                <li><strong>E</strong> stands for Evolving: The abnormal mole appears to be changing in shape, size, or colour.</li>
                            </ul>
                            
                            <p>It is also advisable to get your moles examined if you notice that the mole has appeared first in adulthood, and shows signs of bleeding, oozing, itching, or is scaly in appearance.</p>
                            <p>Your doctor may use dermatoscopy or epiluminescence microscopy (ELM), a hand-held microscope to evaluate the surface of the skin and detect the cancerous nature of the mole. This is a more accurate diagnostic tool. Your doctor may also conduct a biopsy, where a suspicious section of the skin is removed and is sent to the lab for microscopic examination.</p>
                            
                            <h2>Risk factors for Skin Cancer</h2>
                            <p>The risk of developing melanoma is exceptionally high in individuals with</p>
                            <ul>
                                <li>More than 50 common moles</li>
                                <li>Atypical mole syndrome: more than 100 moles, among which one or more looks abnormal</li>
                                <li>Familial atypical multiple mole melanoma (FAMMM) syndrome: condition inherited from a member of the family who has melanoma. An individual has many moles, some of which may look abnormal.</li>
                            </ul>
                            
                            <h2>Skin Cancer Prevention Tips</h2>
                            <p>Timely and adequate preventive measures can go a long way in lowering the incidence of skin cancer. Make it a habit to shield yourself from the dangerous ultra-violet rays of the sun by staying indoors during peak hours, wearing sun-protective gear and clothing, and applying broad-spectrum sunscreen lotion with a minimum 15 SPF (sun protection factor) at least thirty minutes prior to going out in the sun.</p>

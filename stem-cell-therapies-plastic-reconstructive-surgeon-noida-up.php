                            <h2>Stem Cell procedure </h2>
                            <p>The procedure begins with your doctor extracting stem cells from your own bone marrow. Bone marrow is usually aspirated from your hip region. Your doctor will first clean and numb your hip area. A needle is then introduced into an area of your pelvic bone known as the iliac crest. Bone marrow is then aspirated using a special syringe and the sample obtained is sent to the laboratory. In the laboratory, the aspirate is spun in a machine for 10 to 15 minutes and a concentrated stem cell sample is separated. Your doctor then cleans and numbs your affected area to be treated and then, under the guidance of special x-rays, injects the stem cells into the diseased region. The whole procedure usually takes less than one hour and you may return home on the same day of the procedure.</p>
                            

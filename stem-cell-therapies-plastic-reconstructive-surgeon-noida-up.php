                            <h2>Introduction </h2>
                            <p>Stem cell therapies are a form of regenerative medicine that utilise the body’s natural healing mechanism to treat various conditions. Stem cells are being used in regenerative medicine to renew and repair diseased or damaged tissues, and have shown promising results in treatments of various orthopaedic, cardiovascular, neuromuscular and autoimmune conditions. Stem cells are present in all of us acting like a repair system for the body. However, with increased age sometimes the optimum amount of stem cells are not delivered to the injured area. The goal of Stem Cell therapy is to amplify the natural repair system of the patient’s body.</p>

                            <h2>Types of Stem Cells </h2>
                            
                            <p>There are two major types of stem cells, embryonic stem cells and adult stem cells. Embryonic stem cells (ESCs) are stem cells derived from human embryos. They are pluripotent, which means they have the ability to develop into almost any of the various cell types of the body. </p>
                            
                            <p>As the embryo develops and forms a baby, stem cells are distributed throughout the body where they reside in specific pockets of each tissue, such as the bone marrow and blood. As we age, these cells function to renew old and worn out tissue cells. These are called adult stem cells or somatic stem cells. Like embryonic stem cells, adult stem cells can also replicate into more than one cell type, but their replication is restricted to a limited number of cell types.</p>
                            
                            <h2>Use of Stem Cell Therapies in Orthopaedics</h2>
                            <p>The unique self-regeneration and differentiating ability of embryonic stem cells can be used in regenerative medicine. These stem cells can be derived from eggs collected during IVF procedures with informed consent from the patient. However, many questions have been raised on the ethics of destroying a potential human life for the treatment of another. Adult stem cells are most commonly obtained from the bone marrow, specifically the mesenchymal stem cells, which have the ability to replicate into cells that form the musculoskeletal system such as tendons, ligaments, and articular cartilage. They can be obtained from the iliac crest of the pelvic bone by inserting a needle and extracting the stem cells from the bone marrow.</p>
                            <p>Currently, stem cell therapy is used to treat various degenerative conditions of the shoulder, knees, hips, and spine. They are also being used in the treatment of various soft tissues (muscle, ligaments, and tendons) as well as bone-related injuries.</p>
                            
                            <h2>Who is a Good Candidate for Stem Cell Therapies? </h2>

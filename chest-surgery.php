                        <article class="textMain ypocms"><p>Pectus deformity is the most common congenital chest wall deformity. It is often noticeable at birth but usually becomes more apparent during puberty. When the chest wall develops, the cartilage that connects the ribs usually grows flat along the chest. In cases of pectus, this cartilage grows abnormally, causing unequal growth in the areas where the ribs connect to the sternum. These may be protrusions/depressions/asymmetries and also syndromic or non syndromic, usually present for consultation around adolescence when the person becomes aware about its presence or has difficulty in breathing or other related problems. It is more common in males.</p>

<p>There are two main types of pectus deformities</p>

<ol>
	<li><strong>Pectus carinatum</strong> (also known as pigeon chest) where the sternum is raised and the chest looks pushed out. Sometimes there may be a depression (dip) on one side and protrusion (bulge) on the other, or the anomaly can be more complex than this.</li>
	<li><strong>Pectus excavatum</strong> (also known as funnel chest or sunken chest) where the sternum is sunken inwards and the chest looks hollow.</li>
</ol>

<h2>Symptoms</h2>

<p>Aesthetic concern is one of the major reason but some people may report other symptoms, such as:</p>

<ul>
	<li>shortness of breath, which is more common during exercise</li>
	<li>tenderness or pain in the areas of abnormal cartilage growth</li>
	<li>frequent respiratory infections</li>
	<li>chest pain</li>
	<li>rapid heart rate</li>
	<li>tiredness</li>
	<li>heart disease</li>
	<li>abnormal curvature of the spine</li>
	<li>kyphosis, where the back is hunched</li>
	<li>other musculoskeletal abnormalities</li>
	<li>connective tissue disorders, such as Marfan syndrome</li>
	<li>abnormal growth patterns</li>
	<li>Sometimes, there may be psychological effects, such as negative body self-image and low self-confidence.</li>
</ul>

<h2>What should you expect during the consultation?</h2>

<p>You will <u>meet our team members</u>, and your surgeon will personally evaluate your condition and will ask about your health history. Radiological investigation like CT and Chest X-ray is usually prescribed to understand the bony structure. These imaging tests can help with planning of surgery also. We may also request PFT if the condition is causing respiratory symptoms and/or ECG and ECHO who experience symptoms related to the heart. You will have an opportunity to discuss treatment options and the recommended course of treatment, explore outcomes of procedures and the potential risks and complications associated and get your questions answered.</p>

<h2>What should you expect during the treatment?</h2>

<p>Surgical management is the preferred treatment in young patients presenting around adolescent age. In few motivated patients and children, costal cartilage deformity may be reversed by braces and splints, when presenting early. Patients presenting after 12 to 15 years benefit best by surgical intervention with better aesthetic outcomes. The anesthesia team assesses your medical fitness before surgery. The doctors can prescribe some diagnostic testing and medications. Once the medical fitness is declared, instructions for the day of surgery are given. Post surgery, patients are advised to undergo graded &amp; supervised rehabilitation to regain &amp; improve chest dynamics as well as cardiac &amp; pulmonary function. The benefits &amp; results are evident immediately &amp; are long lasting. This is an extremely safe and rewarding procedure.</p>

<h2>What should you expect during the recovery?</h2>

<p>There can be some discomfort, swelling, and pain after surgery. Post-surgery medications are prescribed for faster recovery. Our team will advise you on the care of the surgical site and feeding or activity restrictions that may be necessary to promote normal healing. Also, you can discuss specific issues to be monitored in the general health of your patient during recovery with your doctor. Periodic follow-ups are advised to monitor the patient&rsquo;s health status.</p>


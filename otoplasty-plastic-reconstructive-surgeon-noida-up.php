                            <p>An otoplasty is a surgical procedure performed to change the size and shape of the ear. It is also known as cosmetic ear surgery. The location of the ear or the ability to hear is not affected by the surgery. Otoplasty is usually done after the ears have reached their full size and shape; however, it may be performed as early as 3 years old if there is a noticeable ear deformity that cannot be corrected with non-surgical treatments such as splinting. </p>
                            


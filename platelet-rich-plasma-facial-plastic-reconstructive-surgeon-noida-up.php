                            <p>Platelet-rich plasma or PRP is a substance obtained from processed blood. It has remarkable healing and regenerative properties and can be used for cosmetic purposes.</p>
                            <p>Blood consists of a liquid component known as plasma and solid components which include the red blood cells (RBCs), white blood cells (WBCs), and platelets. Platelets play an important role in forming blood clots. They also contain special proteins, known as growth factors, which help with our body’s healing process. Platelet-rich plasma is processed from your own blood and consists of plasma with a high concentration of platelets. A normal blood specimen contains only 6% platelets, while platelet-rich plasma contains 94% platelets. It also has 5 to 10 times the concentration of growth factors found in normal blood.</p>
                            <p>To obtain PRP, a small amount of your blood is drawn from your arm and spun in a special machine called a centrifuge to separate the plasma and other blood components including the platelets. Plasma with a high concentration of platelets is then isolated and kept aside in a sterile syringe to be injected.</p>

